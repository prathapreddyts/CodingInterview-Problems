package revision.binarysearch;

public class Main {
    public static void main(String[] args) {
        int start=0;
        int end=7;
        while (start<=end){
            System.out.println("start : "+start);
            System.out.println("end : "+end);
            start++;
            end--;
        }
        System.out.println("final start : "+start);
        System.out.println("final end : "+end);

    }
}
